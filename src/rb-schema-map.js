export const RB_TABLE_TO_SECTIONS = Object.freeze(RB_SECTIONS.reduce((map, section) => {
  section.tables.forEach((table) => {
    map[table] ||= [];
    map[table].push(section.key);
  });
  return map;
}, {}));

